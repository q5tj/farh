/**
 * Post-export Vercel adapter.
 *
 * Definitive root cause from the deploy logs:
 *   Error: No entrypoint found in output directory: "artifacts/farah/dist"
 *
 * The Vercel project dashboard is configured (in a way we cannot
 * see or change from CI) to invoke `@vercel/node` on
 * `artifacts/farah/dist`, looking for an HTTP handler entrypoint.
 * Build Output API (.vercel/output/config.json) at either the repo
 * root or `artifacts/farah/` does NOT prevent that — Vercel runs
 * @vercel/node anyway and fails before it ever inspects the marker.
 *
 * Solution: stop fighting it. Hand Vercel a real entrypoint
 * (`index.js`) inside the dist directory that wraps every request
 * with a tiny static-file server pointed at __dirname. Vercel
 * deploys it as a single serverless function, the function serves
 * the entire SPA (HTML, JS, fonts, assets) with proper MIME types
 * and SPA fallback to index.html for unknown routes.
 *
 * Also still emits the .vercel/output/ Build Output API tree at both
 * roots and dist/public/build mirrors as backup paths in case the
 * dashboard config is later corrected.
 */

const fs = require("fs");
const path = require("path");

const farahDir = path.resolve(__dirname, "..");
const repoRoot = path.resolve(farahDir, "..", "..");
const sourceDist = path.join(farahDir, "dist");

if (!fs.existsSync(sourceDist)) {
  console.error(`[sync] expo export output not found at ${sourceDist}`);
  process.exit(1);
}

// 1. Drop a serverless-function entrypoint into dist/. The handler
//    serves files relative to its own directory, with an SPA
//    fallback to index.html. Vercel's @vercel/node runtime picks
//    this up as the entry it was looking for.
const HANDLER_SOURCE = `// Auto-generated by scripts/post-export-sync.js
// Serves the bundled Expo web export as a single serverless function.
const fs = require("fs");
const path = require("path");

const STATIC_ROOT = __dirname;

const MIME = {
  ".html": "text/html; charset=utf-8",
  ".js":   "application/javascript; charset=utf-8",
  ".mjs":  "application/javascript; charset=utf-8",
  ".css":  "text/css; charset=utf-8",
  ".json": "application/json; charset=utf-8",
  ".map":  "application/json; charset=utf-8",
  ".txt":  "text/plain; charset=utf-8",
  ".xml":  "application/xml; charset=utf-8",
  ".svg":  "image/svg+xml",
  ".png":  "image/png",
  ".jpg":  "image/jpeg",
  ".jpeg": "image/jpeg",
  ".gif":  "image/gif",
  ".webp": "image/webp",
  ".ico":  "image/x-icon",
  ".ttf":  "font/ttf",
  ".otf":  "font/otf",
  ".woff": "font/woff",
  ".woff2":"font/woff2",
  ".wasm": "application/wasm",
};

function serveFile(res, filePath) {
  const ext = path.extname(filePath).toLowerCase();
  const mime = MIME[ext] || "application/octet-stream";
  const stat = fs.statSync(filePath);
  res.setHeader("Content-Type", mime);
  res.setHeader("Content-Length", stat.size);
  // Hashed bundle assets are immutable.
  if (filePath.includes(path.sep + "_expo" + path.sep) ||
      filePath.includes(path.sep + "assets" + path.sep)) {
    res.setHeader("Cache-Control", "public, max-age=31536000, immutable");
  } else {
    res.setHeader("Cache-Control", "public, max-age=0, must-revalidate");
  }
  fs.createReadStream(filePath).pipe(res);
}

module.exports = (req, res) => {
  try {
    let pathname;
    try {
      pathname = decodeURIComponent(new URL(req.url, "http://x").pathname);
    } catch {
      pathname = req.url.split("?")[0] || "/";
    }
    if (pathname === "/" || pathname === "") pathname = "/index.html";

    let filePath = path.join(STATIC_ROOT, pathname);

    // Contain to STATIC_ROOT to block ../ traversal.
    const rel = path.relative(STATIC_ROOT, filePath);
    if (rel.startsWith("..") || path.isAbsolute(rel)) {
      res.statusCode = 403;
      return res.end("Forbidden");
    }

    let stat;
    try {
      stat = fs.statSync(filePath);
    } catch {
      // SPA fallback for any non-file path.
      return serveFile(res, path.join(STATIC_ROOT, "index.html"));
    }
    if (stat.isDirectory()) {
      const indexInDir = path.join(filePath, "index.html");
      if (fs.existsSync(indexInDir)) return serveFile(res, indexInDir);
      return serveFile(res, path.join(STATIC_ROOT, "index.html"));
    }
    return serveFile(res, filePath);
  } catch (e) {
    console.error("[handler]", e && e.stack ? e.stack : e);
    res.statusCode = 500;
    res.end("Internal Server Error");
  }
};
`;

const handlerPath = path.join(sourceDist, "index.js");
fs.writeFileSync(handlerPath, HANDLER_SOURCE);
console.log(`[sync] ✓ ${handlerPath} (serverless entrypoint)`);

// 2. Mirror the dist tree to legacy locations and Build Output API
//    layout — these stay in place as fallback for any future
//    deploy where the dashboard config is corrected.
function copyDir(target) {
  if (path.resolve(target) === path.resolve(sourceDist)) return false;
  try {
    if (fs.existsSync(target)) {
      fs.rmSync(target, { recursive: true, force: true });
    }
    fs.cpSync(sourceDist, target, { recursive: true });
    console.log(`[sync] ✓ ${target}`);
    return true;
  } catch (e) {
    console.warn(`[sync] ✗ ${target}: ${e.message}`);
    return false;
  }
}

function writeBuildOutputApi(baseDir) {
  const outputApiDir = path.join(baseDir, ".vercel", "output");
  fs.mkdirSync(outputApiDir, { recursive: true });
  copyDir(path.join(outputApiDir, "static"));

  const config = {
    version: 3,
    routes: [
      { handle: "filesystem" },
      { src: "^/(.*)$", dest: "/index.html" },
    ],
  };
  fs.writeFileSync(
    path.join(outputApiDir, "config.json"),
    JSON.stringify(config, null, 2),
  );
  console.log(`[sync] ✓ ${path.join(outputApiDir, "config.json")}`);
}

writeBuildOutputApi(repoRoot);
writeBuildOutputApi(farahDir);

for (const baseDir of [repoRoot, farahDir]) {
  for (const name of ["dist", "public", "build"]) {
    copyDir(path.join(baseDir, name));
  }
}

console.log("[sync] Done. Serverless entrypoint + Build Output API + legacy mirrors all emitted.");
